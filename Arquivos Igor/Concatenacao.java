class Concatenacao {
    public static void main(String[] args) {
        String string1 = new String("Tiago");
        String string2 = " ";
        String string3 = new String("Careca");
        int um = 1;
        float dois = 2.0f;
        long tres = 3;
        double quatro = 4.0;

        System.out.print(string1+string2+string3 + " " + um+ " " + dois + " " + tres + " " + quatro);
        System.out.println();
    }
}